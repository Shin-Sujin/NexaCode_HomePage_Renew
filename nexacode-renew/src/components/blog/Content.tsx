"use client";

import { useEffect } from "react";
import feather from "feather-icons";
import { blogList } from "./blogItem";

export default function Content({ index }: { index: number }) {
  useEffect(() => {
    feather.replace();
  }, []);

  const ContentComponent = blogList[index]?.content;
  if (!ContentComponent) return <div>존재하지 않는 글입니다.</div>;
  return <ContentComponent />;
}
